<H1 >Charte associative</H1>

<div id="acte1">
<dl>
<dt>Art 1 :</dt>
<dd>Le site de l'Association Lorraine pour le Développement du Numérique, propose de mettre en relation employeurs et chercheurs d'emploi
 dans le cadre d'un partenriat intergénérationnel.</dd>
 
<dt>Art 2 :</dt>
<dd>définition d'un partenariat intergénérationnel :<br>
C'est la constitution ou l'amélioration d'équipes intégrant des classes d'âges différentes.</dd>

<dt>Art 3 :</dt>
<dd>Il est instauré un service de mise en relation gratuite. Ce service est interdit aux cabinets de recrutements, résérvé aux chercheurs d'emploi
 et aux entreprises, sans restriction et localistion géographique.</dd>

<dt>Art 4 :</dt>
<dd>Il est instauré un service payant : ''Confiez un recrutement''. Le site ALDN est détenteur d'une obligation de moyens, dans la recherche et
la découverte de futur candidat. En cas de réussite un honoraire d'une valeur d'un mois de salaire (BRUT) du salarié recruté sera versé par l'employeur.
 Si le recruteur est un mandataire de quelque nature juridique possible l'honoraire sera l'équivalent de 1,8 mois de salaire du salarié recruté.</dd>
 
 <dt>Art 5 :</dt>
 <dd>Le service ''confier un recrutement'' est sans restriction de nature juridique des offreurs d'emploi ni de restriction geographique.</dd>
<dt>Art 6 :</dt>
<dd>Le service ALDN n'est pas responsable  des difficultées qui pourraient survenir après le recrutement en cas de mensonge des candidats ou des employeurs.</dd>
<dt>Art 7 :</dt>
<dd>Pour les entreprises Françaises et ou les candidats Français, les tribunaux Français sont seuls compétents, en cas de litige.</dd>
<dt>Art 8 :</dt>
<dd>Aucun paiements ne sera effectué avant le recrutement. Sauf accord entre le recruteur et le site ALDN.</dd>
</dl>

